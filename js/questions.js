/** Losuje nieużyte pytanie z kategorii. Zwraca {pytanie, odpowiedz, index} lub null. */
export function pickQuestion(questions, category, usedQuestions) {
  const pool = questions[category] ?? [];
  if (pool.length === 0) return null;
  const used = usedQuestions[category] ?? [];
  let available = pool.map((_, i) => i).filter(i => !used.includes(i));
  if (available.length === 0) available = pool.map((_, i) => i); // reset gdy wszystkie użyte
  const idx = available[Math.floor(Math.random() * available.length)];
  return { ...pool[idx], index: idx };
}

/** Walidacja bazy pytań. Zwraca { ok, cleaned, errors }. */
export function validateQuestions(data) {
  const errors = [];
  if (!data || typeof data !== 'object' || Array.isArray(data)) {
    return { ok: false, cleaned: null, errors: ['Baza musi być obiektem { kategoria: [...] }'] };
  }
  const cleaned = {};
  for (const cat of Object.keys(data)) {
    const arr = data[cat];
    if (!Array.isArray(arr)) { errors.push(`"${cat}" nie jest listą`); continue; }
    const items = [];
    arr.forEach((it, i) => {
      if (!it || typeof it.pytanie !== 'string' || !it.pytanie.trim()) { errors.push(`"${cat}"[${i}]: brak pytania`); return; }
      if (typeof it.odpowiedz !== 'string' || !it.odpowiedz.trim()) { errors.push(`"${cat}"[${i}]: brak odpowiedzi`); return; }
      items.push({ pytanie: it.pytanie.trim(), odpowiedz: it.odpowiedz.trim() });
    });
    if (items.length) cleaned[cat] = items;
  }
  if (Object.keys(cleaned).length === 0) return { ok: false, cleaned: null, errors: errors.length ? errors : ['Brak poprawnych pytań'] };
  return { ok: true, cleaned, errors };
}
